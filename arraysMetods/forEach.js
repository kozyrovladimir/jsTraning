const myArr = [5, 6, 58, 41, 12, 94]; // Массив чисел

let newArr = []; // Пустой массив

myArr.forEach((item) => newArr.push(item * 2)); // Метод массива forEach, который добавляет в пустой макссив newArr каждый элемент массива newArr увеличенный вдвое

console.log(newArr); // Выводим измененный массив newArr в консоль
